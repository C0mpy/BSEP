package com.timsedam.dto;

public class LogDTO {
	private String agentId;
	private String log;
	
	public LogDTO(){}
	
	public String getAgentId() {
		return agentId;
	}

	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}

	public String getLog() {
		return log;
	}

	public void setLog(String log) {
		this.log = log;
	}
	
	
}
